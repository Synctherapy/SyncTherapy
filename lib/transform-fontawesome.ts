export function transformFontAwesome(html: string): string {
    // 1. Remove inline FontAwesome CSS link tags
    let result = html.replace(
        /<link[^>]*href=["'](?:https:\/\/cdnjs\.cloudflare\.com\/ajax\/libs\/font-awesome|https:\/\/use\.fontawesome\.com)[^>]*>/gi,
        ''
    );

    // 2. Replace <i class="fas fa-{icon} ..."> with inline SVGs
    result = result.replace(
        /<i\s+class=["']fas\s+(fa-[a-z-]+)([^"']*)["'][^>]*><\/i>/gi,
        (_match, iconClass: string, extraClasses: string) => {
            const icon = FA_ICON_MAP[iconClass];
            if (!icon) {
                // Unknown icon — render empty span to preserve layout
                return `<span class="${iconClass}${extraClasses}" aria-hidden="true"></span>`;
            }

            // Preserve extra Tailwind classes (text color, size, margins, etc.)
            const classes = extraClasses.trim();
            return `<svg class="inline-block ${classes}" viewBox="0 0 ${icon.vw} 512" fill="currentColor" aria-hidden="true" style="width:1em;height:1em"><path d="${icon.path}"/></svg>`;
        }
    );

    return result;
}
